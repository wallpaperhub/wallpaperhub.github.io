                <h1>Bessie Coleman: The First Black Woman Pilot</h1>
                <p>Bessie Coleman was a pioneering African American aviator and the first woman of African-American and Native American descent to hold a pilot's license. Born in 1892, she overcame educational and racial barriers to pursue her passion for flying. Her determination and achievements inspired generations, paving the way for future aviators.</p>
