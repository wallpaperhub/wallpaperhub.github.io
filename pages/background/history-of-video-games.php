                <h1>The Evolution of Video Games: A Brief History</h1>
                <p>The history of video games dates back to the 1950s with early experiments in computer graphics and simple games like "Tennis for Two." The 1970s introduced arcade hits like "Pong," while the 1980s brought home consoles and iconic titles like "Pac-Man." The industry evolved through computer gaming in the 1990s to today’s immersive experiences and eSports.</p>
