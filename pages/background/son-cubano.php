                <h1>Descubre el Rítmico Son Cubano: Raíces de la Salsa</h1>
                <p>El son cubano es un género musical originario de Cuba, que combina ritmos africanos y melodías españolas. Popularizado en el siglo XX, el son se caracteriza por sus instrumentos como la guitarra, el tres, y las maracas. Este estilo vibrante y contagioso ha influenciado a otros géneros latinos, siendo la raíz de la salsa y el jazz afrocubano.</p>
