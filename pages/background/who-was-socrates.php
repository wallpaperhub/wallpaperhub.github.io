                <h1>Who Was Socrates? Understanding the Philosopher</h1>
                <p>Socrates was an ancient Greek philosopher credited as one of the founders of Western philosophy. Known for his method of questioning, known as the Socratic method, he focused on ethics and the examination of moral character. Socrates did not write down his teachings; instead, they were documented by students like Plato.</p>
