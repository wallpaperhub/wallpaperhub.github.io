                <h1>Origin of Pumpkin Pie: When Was It First Made?</h1>
                <p>Pumpkin pie has roots dating back to the early 17th century, coinciding with the arrival of European settlers in North America. The dessert evolved from indigenous practices, utilizing native pumpkins and spices. The first known recipe appeared in a 1796 cookbook by Amelia Simmons, marking its rise as a beloved holiday staple.</p>
