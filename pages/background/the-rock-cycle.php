                <h1>Understanding the Rock Cycle: Nature's Dynamic Process</h1>
                <p>The rock cycle is a continuous process that describes the transformation of rocks through various geological stages. It includes igneous, sedimentary, and metamorphic rocks, each formed through unique processes like cooling, erosion, and heat/pressure. Understanding the rock cycle is key to comprehending Earth’s dynamic crust and its constant change.</p>
