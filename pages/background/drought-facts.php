                <h1>Essential Drought Facts: Understanding the Impact</h1>
                <p>Droughts are prolonged periods of abnormally low rainfall, leading to water shortages. They can cause severe agricultural damage, increased food prices, and habitat loss. Droughts may last months or years and are exacerbated by climate change. Early warning systems and sustainable water management are crucial for mitigation.</p>
