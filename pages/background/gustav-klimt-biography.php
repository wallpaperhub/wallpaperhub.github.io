                <h1>Gustav Klimt: Life and Legacy of a Master Painter</h1>
                <p>Gustav Klimt (1862-1918) was an Austrian symbolist painter, celebrated for his distinctive style that fused symbolism and decorative elements. A key figure in the Vienna Secession movement, Klimt's most famous works, like "The Kiss" and "Portrait of Adele Bloch-Bauer I," showcase his use of gold leaf and intricate patterns, exploring themes of love and femininity.</p>
