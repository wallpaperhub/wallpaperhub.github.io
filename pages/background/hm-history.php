                <h1>Explore the Fascinating History of H&M</h1>
                <p>H&M, founded in 1947 by Erling Persson in Sweden, began as a women's clothing store called Hennes, meaning "hers." In 1968, it acquired a hunting apparel store, adding men's clothing to its offerings. Today, H&M stands as a global fashion giant, known for affordable, trendy clothing and its commitment to sustainability and ethical practices.</p>
